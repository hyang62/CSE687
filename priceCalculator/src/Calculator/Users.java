package Calculator;

public class Users {
	int id;
	String userName;
	String email;
	String phone;
	Location location;
	
	public Users() {}
	public Users(int id, String userName, String email, String phone, Location location) {
		this.id = id;
		this.userName = userName;
		this.email = email;
		this.phone = phone;
		this.location = location;
	}
	
}


